#include <crpcut.hpp>

namespace crpcut {
  istream_wrapper
  &istream_wrapper
  ::assert_stream_extraction(std::istream &is, const char *msg)
  {
    if (!is)
      {
        crpcut_test_monitor *current_test = crpcut_test_monitor::current_test();
        comm::report(comm::exit_fail, msg, current_test->get_location());
      }
    return *this;
  }
}
